import pandas as pd
import numpy as np
from sklearn import datasets, linear_model

mtcars = pd.read_csv('https://raw.githubusercontent.com/natelangholz/stat418-tools-in-datascience/master/week-6/hw3/mtcars.csv')

mpg = mtcars['mpg']
train1 = mtcars.iloc[:, 3:8]

col_imp = ["disp", "hp", "drat", "wt", "qsec"]

regr = linear_model.LinearRegression()
regr.fit(train1, mpg)

def predict(dict_values, col_imp = col_imp, regr = regr):
  x = np.array([float(dict_values[col]) for col in col_imp])
  x = x.reshape(1,-1)
  y_pred = regr.predict(x)[0]
  return y_pred